    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request);
        $request->validate([
            'name' => 'required',
            'email' => 'required|email',
            'message' => 'required',
        ]);

        $contact = new Contact();

        $contact->name = $request->input('name');
        $contact->email = $request->input('email');
        $contact->subject = $request->input('subject');
        $contact->message = $request->input('message');
        $contact->ticket_id = uniqid();

        if($contact->save()){
            $companyMail = 'asuarb@icloud.com';
            Mail::to($request->input('email'))->send(new ContactSent($request, $contact->ticket_id));
            Mail::to($companyMail)->send(new ContactReceived($request, $contact->ticket_id));
            return back()->withMessage('Contact Sent Successfully');
        }else{
            return back()->withError('Contact Sent Successfully');
        }
    }

